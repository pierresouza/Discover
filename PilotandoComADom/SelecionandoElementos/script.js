//getElementById()

const firstElement = document.getElementById("one");
console.log(firstElement);

//getElementByClassName()
const secondElement = document.getElementsByClassName("one");
console.log(secondElement);

//getElementByTagName()

const thirdElement = document.getElementsByTagName("h1");
console.log(thirdElement);

//querySelector()

const forthElement = document.querySelector(".one");
console.log(forthElement);

//querySelectorAll()

const fifthElement = document.querySelectorAll(".one");
console.log(fifthElement);

